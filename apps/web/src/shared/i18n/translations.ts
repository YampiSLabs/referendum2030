export function useTranslations(lang: Locale) {
  return function t(key: string): string {
    const resolvedKey = KEY_MAPPING[key] || key;
    
    if (customUi[lang]?.[resolvedKey as keyof typeof customUi["ca"]]) {
      return customUi[lang][resolvedKey as keyof typeof customUi["ca"]];
    }
    if (customUi["ca"]?.[resolvedKey as keyof typeof customUi["ca"]]) {
      return customUi["ca"][resolvedKey as keyof typeof customUi["ca"]];
    }
    
    return ui[lang][resolvedKey as keyof typeof ui["ca"]] || ui["ca"][resolvedKey as keyof typeof ui["ca"]] || key;
  };
}

