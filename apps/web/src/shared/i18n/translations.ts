export function translateOptionLabel(caLabel: string, optionIdOrLang: number | Locale, possibleLang?: Locale): string {
  let optionId: number | undefined = undefined;
  let lang: Locale = "ca";
  
  if (typeof optionIdOrLang === "number") {
    optionId = optionIdOrLang;
    lang = possibleLang || "ca";
  } else {
    lang = optionIdOrLang;
  }

  const optionsDict: Record<number, Record<Locale, string>> = {
    1: { ca: "Sí", es: "Sí", en: "Yes", fr: "Oui", ar: "نعم", zh: "是" },
    2: { ca: "No", es: "No", en: "No", fr: "Non", ar: "لا", zh: "否" },
    3: { ca: "En blanc", es: "En blanco", en: "Blank", fr: "Blanc", ar: "أبيض", zh: "空白" }
  };
  
  if (optionId && optionsDict[optionId]) {
    return optionsDict[optionId][lang] || optionsDict[optionId]["ca"];
  }
  
  // fallback mapping if optionId is not reliable but label matches ca defaults
  const cleanLabel = caLabel.trim().toLowerCase();
  if (cleanLabel === "sí" || cleanLabel === "si") {
    return optionsDict[1][lang];
  }
  if (cleanLabel === "no") {
    return optionsDict[2][lang];
  }
  if (cleanLabel === "en blanc" || cleanLabel === "blanc" || cleanLabel === "en blanco") {
    return optionsDict[3][lang];
  }
  
  return caLabel;
}

